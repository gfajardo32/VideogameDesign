using UnityEngine;
using UnityEditor;

/// Wires DropObstacle onto existing NPCS (SecurityGuard + NeutralShoppers)
/// that were already placed by SceneBuilder/SceneUpdater.
public static class NPCAdder
{
    [MenuItem("GroceryRush/Wire DropObstacle onto NPCs")]
    static void WireDropObstacle()
    {
        int count = 0;

        // ?????? SecurityGuard ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        var guard = GameObject.Find("SecurityGuard");
        if (guard != null && guard.GetComponent<DropObstacle>() == null)
        {
            var drop = Undo.AddComponent<DropObstacle>(guard);
            drop.maxDropCount = 1;
            drop.cooldown     = 6f;
            count++;
            Debug.Log("[NPCAdder] DropObstacle added to SecurityGuard.");
        }

        // ?????? NeutralShoppers ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        var allNS = GameObject.FindObjectsByType<NeutralShopper>(FindObjectsSortMode.None);
        foreach (var ns in allNS)
        {
            if (ns.GetComponent<DropObstacle>() == null)
            {
                var drop = Undo.AddComponent<DropObstacle>(ns.gameObject);
                drop.maxDropCount = 1;
                drop.cooldown     = 4f;
                count++;
                Debug.Log($"[NPCAdder] DropObstacle added to {ns.name}.");
            }
        }

        // ?????? ShopperAI ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        var shoppers = GameObject.FindObjectsByType<ShopperAI>(FindObjectsSortMode.None);
        foreach (var s in shoppers)
        {
            if (s.GetComponent<DropObstacle>() == null)
            {
                var drop = Undo.AddComponent<DropObstacle>(s.gameObject);
                drop.maxDropCount = 1;
                drop.cooldown     = 3f;
                count++;
                Debug.Log($"[NPCAdder] DropObstacle added to {s.name}.");
            }
        }

        // ?????? KidAI ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        var kids = GameObject.FindObjectsByType<KidAI>(FindObjectsSortMode.None);
        foreach (var k in kids)
        {
            if (k.GetComponent<DropObstacle>() == null)
            {
                var drop = Undo.AddComponent<DropObstacle>(k.gameObject);
                drop.maxDropCount = 1;
                drop.cooldown     = 3f;
                count++;
                Debug.Log($"[NPCAdder] DropObstacle added to {k.name}.");
            }
        }

        UnityEditor.SceneManagement.EditorSceneManager.MarkSceneDirty(
            UnityEngine.SceneManagement.SceneManager.GetActiveScene());

        Debug.Log($"[NPCAdder] Done! DropObstacle wired onto {count} NPC(s). Save with Ctrl+S.");
    }
}
