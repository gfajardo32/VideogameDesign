using UnityEngine;
using UnityEditor;
using UnityEditor.SceneManagement;
using System.IO;
using TMPro;

public static class Beautifier
{
    // ?????? Texture Factories ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

    static Texture2D MakeCircleTex(int size)
    {
        var tex    = new Texture2D(size, size, TextureFormat.RGBA32, false);
        var pixels = new Color[size * size];
        var center = new Vector2(size * 0.5f - 0.5f, size * 0.5f - 0.5f);
        float outerR = size * 0.5f - 1f;
        float innerR = outerR - 2f;

        for (int y = 0; y < size; y++)
        for (int x = 0; x < size; x++)
        {
            float d = Vector2.Distance(new Vector2(x, y), center);
            float a = d < innerR ? 1f
                    : d < outerR ? 1f - (d - innerR) / (outerR - innerR)
                    : 0f;
            pixels[y * size + x] = new Color(1f, 1f, 1f, a);
        }
        tex.SetPixels(pixels);
        tex.Apply();
        return tex;
    }

    static Texture2D MakeFloorTex(int texSize, int tileSize)
    {
        var tex    = new Texture2D(texSize, texSize, TextureFormat.RGBA32, false);
        var pixels = new Color[texSize * texSize];
        var tileA  = new Color(0.93f, 0.89f, 0.80f);   // warm cream
        var tileB  = new Color(0.86f, 0.81f, 0.71f);   // slightly darker
        var border = new Color(0.75f, 0.70f, 0.60f);   // tile grout

        for (int y = 0; y < texSize; y++)
        for (int x = 0; x < texSize; x++)
        {
            int tx = x % tileSize, ty = y % tileSize;
            bool isGrout = (tx == 0 || ty == 0);
            bool checker = ((x / tileSize) + (y / tileSize)) % 2 == 0;
            pixels[y * texSize + x] = isGrout ? border : (checker ? tileA : tileB);
        }
        tex.SetPixels(pixels);
        tex.Apply();
        return tex;
    }

    static Sprite SaveAndLoad(Texture2D tex, string assetPath)
    {
        string fullPath = Application.dataPath + assetPath.Replace("Assets", "");
        Directory.CreateDirectory(Path.GetDirectoryName(fullPath));
        File.WriteAllBytes(fullPath, tex.EncodeToPNG());
        AssetDatabase.Refresh();

        var imp = AssetImporter.GetAtPath(assetPath) as TextureImporter;
        if (imp != null)
        {
            imp.textureType            = TextureImporterType.Sprite;
            imp.spriteImportMode       = SpriteImportMode.Single;
            imp.filterMode             = FilterMode.Point;
            imp.textureCompression     = TextureImporterCompression.Uncompressed;
            imp.alphaIsTransparency    = true;
            imp.SaveAndReimport();
        }
        return AssetDatabase.LoadAssetAtPath<Sprite>(assetPath);
    }

    // ?????? Main ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????

    [MenuItem("GroceryRush/Beautify Scene")]
    public static void Beautify()
    {
        // Build sprite assets
        Sprite circleSprite = SaveAndLoad(MakeCircleTex(64),  "Assets/Sprites/circle.png");
        Sprite floorSprite  = SaveAndLoad(MakeFloorTex(512, 32), "Assets/Sprites/floor_tile.png");
        Sprite squareSprite = AssetDatabase.LoadAssetAtPath<Sprite>("Assets/white_square.png");

        // ?????? Floor ???????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        var floorGO = GameObject.Find("Floor");
        if (floorGO != null)
        {
            var sr   = floorGO.GetComponent<SpriteRenderer>() ?? floorGO.AddComponent<SpriteRenderer>();
            sr.sprite       = floorSprite;
            sr.color        = Color.white;
            sr.sortingOrder = -10;
            floorGO.transform.localScale = new Vector3(22f, 20f, 1f);
        }

        // ?????? Shelves ??? add highlight cap ?????????????????????????????????????????????????????????????????????????????????????????????
        var shelvesParent = GameObject.Find("Shelves");
        if (shelvesParent != null)
        {
            foreach (Transform shelf in shelvesParent.transform)
            {
                // Update main shelf color
                var sr = shelf.GetComponent<SpriteRenderer>();
                if (sr != null) sr.color = new Color(0.50f, 0.32f, 0.12f);

                // Add top-cap if not present
                string capName = shelf.name + "_Cap";
                if (shelf.Find(capName) == null)
                {
                    var cap = new GameObject(capName);
                    cap.transform.SetParent(shelf, false);
                    cap.transform.localPosition = new Vector3(0f, 0.5f, 0f);
                    cap.transform.localScale    = new Vector3(1f, 0.12f, 1f);
                    var capSR = cap.AddComponent<SpriteRenderer>();
                    capSR.sprite       = squareSprite;
                    capSR.color        = new Color(0.72f, 0.52f, 0.24f);
                    capSR.sortingOrder = 2;
                }
            }
        }

        // ?????? Items ??? circle sprite + floating label ????????????????????????????????????????????????????????????
        var itemsParent = GameObject.Find("--- ITEMS ---");
        if (itemsParent != null)
        {
            foreach (Transform item in itemsParent.transform)
            {
                var sr = item.GetComponent<SpriteRenderer>();
                if (sr != null) sr.sprite = circleSprite;

                // Ensure ItemPickup script
                if (item.GetComponent<ItemPickup>() == null)
                {
                    var pu = item.gameObject.AddComponent<ItemPickup>();
                    pu.itemName = item.name;
                }

                // Floating label (TMP in world space)
                string lblName = item.name + "_Label";
                if (item.Find(lblName) == null)
                {
                    var lbl = new GameObject(lblName);
                    lbl.transform.SetParent(item, false);
                    lbl.transform.localPosition = new Vector3(0f, 0.55f, 0f);
                    lbl.transform.localScale    = new Vector3(0.35f, 0.35f, 1f);
                    var tmp = lbl.AddComponent<TextMeshPro>();
                    tmp.text      = item.name.ToUpper();
                    tmp.fontSize  = 6f;
                    tmp.alignment = TextAlignmentOptions.Center;
                    tmp.color     = Color.black;
                    tmp.fontStyle = FontStyles.Bold;
                    tmp.sortingOrder = 10;
                }
            }
        }

        // ?????? NPCs ??? circles ????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        var npcsParent = GameObject.Find("--- NPCS ---");
        if (npcsParent != null)
        {
            foreach (Transform npc in npcsParent.transform)
            {
                var sr = npc.GetComponent<SpriteRenderer>();
                if (sr != null) sr.sprite = circleSprite;

                // Ensure AI scripts
                bool isKid = npc.name.StartsWith("Kid");
                if (isKid && npc.GetComponent<KidAI>() == null)
                    npc.gameObject.AddComponent<KidAI>();
                if (!isKid && npc.GetComponent<ShopperAI>() == null)
                    npc.gameObject.AddComponent<ShopperAI>();
                if (npc.GetComponent<Rigidbody2D>() == null)
                {
                    var rb = npc.gameObject.AddComponent<Rigidbody2D>();
                    rb.gravityScale = 0f;
                    rb.constraints  = RigidbodyConstraints2D.FreezeRotation;
                }
            }
        }

        // ?????? Hazards ??? translucent cyan with "WET" label ?????????????????????????????????????????????
        var hazardsParent = GameObject.Find("--- HAZARDS ---");
        if (hazardsParent != null)
        {
            foreach (Transform hz in hazardsParent.transform)
            {
                var sr = hz.GetComponent<SpriteRenderer>();
                if (sr != null)
                {
                    sr.sprite = squareSprite;
                    sr.color  = new Color(0.40f, 0.80f, 1.00f, 0.45f);
                    sr.sortingOrder = 1;
                }

                // Ensure HazardZone
                if (hz.GetComponent<HazardZone>() == null)
                    hz.gameObject.AddComponent<HazardZone>();

                // "WET" label
                string lblName = hz.name + "_Label";
                if (hz.Find(lblName) == null)
                {
                    var lbl = new GameObject(lblName);
                    lbl.transform.SetParent(hz, false);
                    lbl.transform.localPosition = Vector3.zero;
                    lbl.transform.localScale    = new Vector3(0.55f, 0.55f, 1f);
                    var tmp = lbl.AddComponent<TextMeshPro>();
                    tmp.text      = "WET\nFLOOR";
                    tmp.fontSize  = 5f;
                    tmp.alignment = TextAlignmentOptions.Center;
                    tmp.color     = new Color(0f, 0.3f, 0.7f);
                    tmp.fontStyle = FontStyles.Bold;
                    tmp.sortingOrder = 10;
                }
            }
        }

        // ?????? Player ??? add direction dot ????????????????????????????????????????????????????????????????????????????????????????????????
        var playerGO = GameObject.Find("Player");
        if (playerGO != null)
        {
            // Ensure CartController
            if (playerGO.GetComponent<CartController>() == null)
                playerGO.AddComponent<CartController>();

            // Direction dot
            if (playerGO.transform.Find("DirectionDot") == null)
            {
                var dot = new GameObject("DirectionDot");
                dot.transform.SetParent(playerGO.transform, false);
                dot.transform.localPosition = new Vector3(0f, 0.28f, 0f);
                dot.transform.localScale    = new Vector3(0.2f, 0.2f, 1f);
                var dotSR = dot.AddComponent<SpriteRenderer>();
                dotSR.sprite       = squareSprite;
                dotSR.color        = new Color(0.05f, 0.30f, 0.80f);
                dotSR.sortingOrder = 6;
            }
        }

        // ?????? Verify GameManager scripts ????????????????????????????????????????????????????????????????????????????????????????????????
        var gmGO = GameObject.Find("GameManager");
        if (gmGO != null)
        {
            if (gmGO.GetComponent<GameManager>()   == null) gmGO.AddComponent<GameManager>();
            if (gmGO.GetComponent<ShoppingList>()  == null) gmGO.AddComponent<ShoppingList>();
        }

        // ?????? Verify Camera Follow ??????????????????????????????????????????????????????????????????????????????????????????????????????????????????
        var camGO = GameObject.FindWithTag("MainCamera");
        if (camGO != null)
        {
            var cf = camGO.GetComponent<CameraFollow>() ?? camGO.AddComponent<CameraFollow>();
            if (cf.target == null && playerGO != null)
                cf.target = playerGO.transform;
        }

        EditorSceneManager.MarkSceneDirty(UnityEngine.SceneManagement.SceneManager.GetActiveScene());
        Debug.Log("[Beautifier] Scene beautified and all scripts verified!");
    }
}
